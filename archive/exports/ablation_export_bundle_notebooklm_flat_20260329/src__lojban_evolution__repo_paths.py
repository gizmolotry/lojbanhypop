def legacy_doc_path(name: str) -> Path:
    return LEGACY_DOC_PATHS.get(name, REPO_ROOT / name)


def legacy_result_dir(name: str) -> Path:
    return LEGACY_RESULT_ROOTS.get(name, REPO_ROOT / name)


def legacy_result_path(name: str, *parts: str) -> Path:
    return legacy_result_dir(name).joinpath(*parts)


def legacy_dataset_dir(name: str) -> Path:
    return LEGACY_DATA_ROOTS.get(name, REPO_ROOT / name)


def repo_relative(path: Path) -> str:
    return str(path.relative_to(REPO_ROOT)).replace("\\", "/")
